function pushbutton_Callback(~,~,action)
    try
        if isempty(action)
            return;
        end
        
        % Get appdata of main window
        mainWindow = guiGetWindowHandle(mfilename);
        ad = getappdata(mainWindow);
        
        % Get handles of main window
        gh = guihandles(mainWindow);

        switch lower(action)
            case 'next'
                if isempty(ad.control.spectra.visible)
                    return;
                end
                selected = get(gh.visible_panel_listbox,'Value');
                if selected == length(ad.control.spectra.visible)
                    selected = 1;
                else
                    selected = selected + 1;
                end
                set(gh.visible_panel_listbox,'Value',selected);
                ad.control.spectra.active = selected;
                setappdata(mainWindow,'control',ad.control);
                updateAxes();
                updateSliderPanel();
            case 'prev'
                if isempty(ad.control.spectra.visible)
                    return;
                end
                selected = get(gh.visible_panel_listbox,'Value');
                if selected == 1
                    selected = length(ad.control.spectra.visible);
                else
                    selected = selected - 1;
                end
                set(gh.visible_panel_listbox,'Value',selected);
                ad.control.spectra.active = selected;
                setappdata(mainWindow,'control',ad.control);
                updateAxes();
                updateSliderPanel();
            case 'fitparameters'
                TAgui_fit_parameterwindow();
            case 'fit'
                if strcmp(ad.fft.display,'time')
                    ad.fft.display = 'frequency';
                    set(gh.fft_pushbutton,'String','F -> t');
                    set(gh.fft_pushbutton,'TooltipString',...
                        'Switch from frequency to time domain (Ifit)');
                    set(findall(allchild(pp2),'-not','Type','uipanel'),...
                        'Enable','On');
                    set(findall(allchild(pp3),'-not','Type','uipanel'),...
                        'Enable','On');
                else
                    ad.fft.display = 'time';
                    set(gh.fft_pushbutton,'String','t -> F');
                    set(gh.fft_pushbutton,'TooltipString',...
                        'Switch from time to frequency domain (fit)');
                    set(findall(allchild(pp2),'-not','Type','uipanel'),...
                        'Enable','Inactive');
                    set(findall(allchild(pp3),'-not','Type','uipanel'),...
                        'Enable','Inactive');
                end
                setappdata(mainWindow,'fft',ad.fft);
                updateAxes();
                return;
            case 'showmaximum'
                % If no datasets are loaded, return
                if isempty(ad.data)
                    return;
                end
                [~,ximax] = max(max(ad.data{ad.control.spectra.active}.data));
                [~,yimax] = max(ad.data{ad.control.spectra.active}.data(:,ximax));
                ad.data{ad.control.spectra.active}.display.position.x = ximax;
                ad.data{ad.control.spectra.active}.display.position.y = yimax;
                
                % Set appdata from BLC GUI
                setappdata(mainWindow,'data',ad.data);
                
                updateAxes();
                update_position_display();
                return;
            case 'zoomreset'
                zoom(mainWindow,'off');
                set(gh.zoom_x_togglebutton,'Value',0);
                set(gh.zoom_xy_togglebutton,'Value',0);
                updateAxes();
                return;
            case'measureclear'
                set(gh.measure_pick_togglebutton,'Value',0);
                set(gh.measure_x_index_edit,'String','0');
                set(gh.measure_x_unit_edit,'String','0');
                set(gh.measure_y_index_edit,'String','0');
                set(gh.measure_y_unit_edit,'String','0');
                return;
            case 'close'
                msgStr = 'Fit GUI window closed.';
                add2status(msgStr);

                % Look for fit GUI Help window and if its there, close as
                % well
                hHelpWindow = findobj('Tag','TAgui_fit_helpwindow');
                if ishandle(hHelpWindow)
                    delete(hHelpWindow);
                end
                delete(guiGetWindowHandle(mfilename));
            otherwise
                disp('TAgui_fitwindow: pushbutton_Callback(): Unknown action');
                disp(action);
                return;
        end
    catch exception
        try
            msgStr = ['An exception occurred. '...
                'The bug reporter should have been opened'];
            add2status(msgStr);
        catch exception2
            exception = addCause(exception2, exception);
            disp(msgStr);
        end
        try
            TAgui_bugreportwindow(exception);
        catch exception3
            % If even displaying the bug report window fails...
            exception = addCause(exception3, exception);
            throw(exception);
        end
    end
end

